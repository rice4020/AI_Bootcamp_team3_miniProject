// 🗺️ 위경도 → 기상청 격자 좌표(nx, ny) 변환 함수
// 람베르트 정각원추 도법 기반 (기상청 공식 수식)
// ============================================================
export function convertLatLngToGrid(lat, lng) {
  const RE = 6371.00877;
  const GRID = 5.0;
  const SLAT1 = 30.0;
  const SLAT2 = 60.0;
  const OLON = 126.0;
  const OLAT = 38.0;
  const XO = 43;
  const YO = 136;
  const DEGRAD = Math.PI / 180.0;

  const re = RE / GRID;
  const slat1 = SLAT1 * DEGRAD;
  const slat2 = SLAT2 * DEGRAD;
  const olon = OLON * DEGRAD;
  const olat = OLAT * DEGRAD;

  let sn = Math.tan(Math.PI * 0.25 + slat2 * 0.5) / Math.tan(Math.PI * 0.25 + slat1 * 0.5);
  sn = Math.log(Math.cos(slat1) / Math.cos(slat2)) / Math.log(sn);
  let sf = Math.tan(Math.PI * 0.25 + slat1 * 0.5);
  sf = Math.pow(sf, sn) * Math.cos(slat1) / sn;
  let ro = Math.tan(Math.PI * 0.25 + olat * 0.5);
  ro = re * sf / Math.pow(ro, sn);

  let ra = Math.tan(Math.PI * 0.25 + lat * DEGRAD * 0.5);
  ra = re * sf / Math.pow(ra, sn);
  let theta = lng * DEGRAD - olon;
  if (theta > Math.PI) theta -= 2.0 * Math.PI;
  if (theta < -Math.PI) theta += 2.0 * Math.PI;
  theta *= sn;

  const x = Math.floor(ra * Math.sin(theta) + XO + 0.5);
  const y = Math.floor(ro - ra * Math.cos(theta) + YO + 0.5);

  return { nx: x, ny: y };
}

// ============================================================
// 📏 사용자 위치에서 가장 가까운 시군구 반환 함수
// 유클리드 거리(위경도 차이)로 가장 가까운 시군구 탐색
// ============================================================
export function findNearestSigungu(lat, lng) {
  let nearest = SIGUNGU_LIST[0];
  let minDistance = Infinity;

  for (const sigungu of SIGUNGU_LIST) {
    const distance = Math.sqrt(
      Math.pow(lat - sigungu.lat, 2) + Math.pow(lng - sigungu.lng, 2)
    );
    if (distance < minDistance) {
      minDistance = distance;
      nearest = sigungu;
    }
  }

  return nearest;
}
